int		ft_atoi(char *str)
{
	int sum;
	int i;
	int negative;

	i = 0;
	sum = 0;
	negative = 0;
	if (str[0] == '+' || str[0] == '-')
	{
		negative = str[0] == '-';
		i++;
	}
	while (str[i] >= '0' && str[i] <= '9')
	{
		sum *= 10;
		sum += str[i] - '0';
		i++;
	}
	if (negative == 1)
	{
		return (sum * -1);
	}
	return (sum);
}
