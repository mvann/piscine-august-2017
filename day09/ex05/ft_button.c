int		ft_button(int i, int j, int k)
{
	if (i == k || i == j)
		return (i);
	else if (k == j)
		return (k);
	if ((j > i && i > k) || (k > i && i > j))
		return (i);
	else if ((k > j && j > i) || (i > j && j > k))
		return (j);
	else
		return (k);
}
